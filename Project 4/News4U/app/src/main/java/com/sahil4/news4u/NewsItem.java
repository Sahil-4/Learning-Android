package com.sahil4.news4u;

public class NewsItem {
    String title;
    String description;
    String url;
    String urlToImage;

    public NewsItem(String title, String description, String url, String urlToImage) {
        this.title = title;
        this.description = description;
        this.url = url;
        this.urlToImage = urlToImage;
    }
}
